
package Modelos;

public class Pilot {

    private String nom;
    private int punts;

    public Pilot(String nom, int punts) {
        this.nom = nom;
        this.punts = punts;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getPunts() {
        return punts;
    }

    public void setPunts(int punts) {
        this.punts = punts;
    }

    public void afegirPunts(int punts) {
        this.punts += punts;
    }

    public String toString() {
        return "Nom: " + nom + " Punts: " + punts;
    }


}
